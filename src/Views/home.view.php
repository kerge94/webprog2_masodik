<h3>Üdvözöljük cégünk weboldalán. Olvasson, használja, élvezze.</h3>
<p>Weboldalunkon regisztrált és bejelentkezett felhasználóval elérhetővé válik egy 
    "leltár" menüpont, ahol a "kategória" menüpont alatt választhat különbözö feladatok közül és
    a weboldal javaslatokat kínál,hogy milyen létező szoftvermegoldások vannak már az adott probléma. </p>
<p>Gyors, egyszerű és magabiztosságot nyújt a bizonytalan felhasználóknak, így elkerülheti a sok teszteléssel járó felesleges bosszankodást.</p>


